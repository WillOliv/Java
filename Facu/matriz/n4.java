package matriz;

public class n4 {

    public static void main(String[] args) {

        /// Estrutura de Repetição (While / contadores e acumuladores)

        int e = 0;
        while (e < 6) {
            System.out.println(e);
            e++;
        }

        ///

        for (int a = 6; a < 11; a++) {
            System.out.println(a);
        }

        ///

        int i = 11;
        do {
            System.out.println(i);
            i++;
        } while (i < 16);
    }
}
