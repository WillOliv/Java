package matriz;

public class n5 {
    public static void main(String[] args) {

        /// Estrutura de Repetição (Aninhamento / Interrompendo Repetição)

        for (int a = 0; a < 5; a++) {

            int b = 1;

            while (b <= 5) {
                System.out.println(b);
                b++;
                break;
            }

            System.out.println("\n");

        }
    }
}
